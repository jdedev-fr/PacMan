function initGrille()
{
    $('.grille').css('grid-template-rows','repeat('+grille.length+', 40px)');
    $('.grille').css('grid-template-columns','repeat('+grille[0].length+', 40px)');

    for(let ligne in grille)
    {
        for(let colonne in grille[ligne])
        {
            let newBloc;
            let grillePosX = (+colonne)+1;
            let grillePosY = (+ligne)+1;
            switch (grille[ligne][colonne])
            {
                case 0:
                    newBloc = $('<div> </div>').addClass("mur").css('grid-column',grillePosX).css('grid-line',grillePosY);
                    break;
                case 1:
                    newBloc = $('<div> </div>').addClass("sol").css('grid-column',grillePosX).css('grid-line',grillePosY);
                    break;
                case 2:
                    newBloc = $('<div> </div>').addClass("bonbon").css('grid-column',grillePosX).css('grid-line',grillePosY);
                    break;
            }
            $('.grille').append(newBloc);
        }
    }
}

